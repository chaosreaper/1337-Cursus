#include "libft.h"

void	ft_putnbr_fd(long long n, int fd)
{
	unsigned int	nb;

	if (fd < 0 )
		return ;
	if (n < 0)
	{
		nb = -n;
		if (n != -2147483648)
		ft_putchar_fd('-', fd);
	}
	else
		nb = n;
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd(nb % 10 + 48, fd);
}
