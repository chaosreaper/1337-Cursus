#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*cp;

	if (!lst)
		return (0);
	cp = lst;
	i = 0;
	while (cp)
	{
		cp = cp->next;
		i++;
	}
	return (i);
}
